package com.rssl.phizic.business.template.offer;

/**
 * @author Balovtsev
 * @since 03.06.2015.
 */
public enum Status
{
	/**
	 * Введен. Статус нового, предстоящего к использованию в будущем шаблона оферты
	 */
	INTRODUCED,

	/**
	 * Действует. Статус шаблона, который является текущим на данный момент
	 */
	OPERATE,

	/**
	 * Архивный. Данный статус устанавливается, если был введен в действие новый шаблон
	 */
	ARCHIVE
}
