package com.rssl.phizic.auth.modes;

/**
 * Режим самостоятельной регистрации изнутри СБОЛ
 * @author basharin
 * @ created 07.05.2013
 * @ $Author$
 * @ $Revision$
 */

public enum RegistrationMode
{
	/**
	 * режим отключен. Самостоятельная регистрация не возможна.
	 */
	OFF,

	/**
	 * «мягкий» режим. Регистрация возможна, но клиент может отказаться.
	 */
	SOFT,

	/**
	 * «жесткий» режим. Регистрация возможна и клиент не может от нее отказаться.
	 */
	HARD;
	
}
