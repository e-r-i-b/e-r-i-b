/**
 *  Режим работы с внешней системой
 */
public enum ConnectMode
{
	/**
	 * Синхроный, взаимодействие инициируется ИКФЛ, ответ на запрос возращается сразу же.
	 */
	SYNC,
	/**
	 * Взаимодействие может инициироваться, как ИКФЛ, так и внешней системой. Ответ на запрос может прийти асинхронно.
	 */
	ASYNC
}
