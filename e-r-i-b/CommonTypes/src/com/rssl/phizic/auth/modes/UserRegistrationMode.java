package com.rssl.phizic.auth.modes;

/**
 * режим самостоятельной регистрации изнутри СБОЛ индивидуально для клиента
 * @author basharin
 * @ created 07.05.2013
 * @ $Author$
 * @ $Revision$
 */

public enum UserRegistrationMode
{
	/**
	 * Используется глобальная настройка системы
	 */
	DEFAULT,

	/**
	 * «мягкий» режим. Регистрация возможна, но клиент может отказаться.
	 */
	SOFT,

	/**
	 * «жесткий» режим. Регистрация возможна и клиент не может от нее отказаться.
	 */
	HARD;
	
}
